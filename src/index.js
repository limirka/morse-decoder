const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let ex = expr
    let trueStr = ""
    while(ex != ''){
        if (ex[0] == '*'){
            ex = ex.substr(10)
            trueStr += " "
        }
        let el = ''
        for( let j = 0; j < 10; j+= 2){
            if(ex[j] == '1' && ex[j+1] == '1'){
                el += '-'
            } 
            if(ex[j] == '1' && ex[j+1] == '0'){
                el += '.'
            }
        }
        trueStr += MORSE_TABLE[el]
        ex = ex.substr(10) 
    }
    return trueStr
}

module.exports = {
    decode
}